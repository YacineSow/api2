    // {
    //     $this->partenaire = $partenaire;

    //     return $this;
    // }

   

    /**
     * Get the value of partenaire
     */ 
    public function getPartenaire()
    {
        return $this->partenaire;
    }

    /**
     * Set the value of partenaire
     *
     * @return  self
     */ 
    public function setPartenaire(int $partenaire)
    {
        $this->partenaire = $partenaire;

        return $this;
    }
}
